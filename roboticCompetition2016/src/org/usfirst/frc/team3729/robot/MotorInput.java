package org.usfirst.frc.team3729.robot;

public class MotorInput {
	private double leftMotorInput;
	private double rightMotorInput;

	public double getLeftMotorInput() {
		return leftMotorInput;
	}

	public void setLeftMotorInput(double leftMotorInput) {
		this.leftMotorInput = leftMotorInput;
	}

	public double getRightMotorInput() {
		return rightMotorInput;
	}

	public void setRightMotorInput(double rightMotorInput) {
		this.rightMotorInput = rightMotorInput;
	}
}
